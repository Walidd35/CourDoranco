/* ----------------------- PRIMITIVE DATA TYPE -----------------------*/

// STRINGS (fr: chaîne de caractères):
const string = 'I am a text';
console.log(string)

// NUMBERS (positive, negative, decimal):
const number = 55;
const negative = -100; 
const decimal = 67.8; 

// BOOLEANS (fr: booléens):
// This type of value only accepts 2 values: True of False
const boolean1 = true; 
const boolean2 = false

const nothing = null

let test; // this type is "undefined"

decimal = null // if we want to reinitialize a variabl, we reassign it to "null"

console.log(typeof string);
console.log(typeof number);
console.log(typeof negative);
console.log(typeof decimal);
console.log(typeof boolean1);
console.log(typeof boolean2);
console.log(typeof test);
console.log(typeof nothing);




